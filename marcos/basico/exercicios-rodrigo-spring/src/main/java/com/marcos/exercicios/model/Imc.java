package com.marcos.exercicios.model;

public class Imc {
    private double weight;
    private double height;
    
    public Imc(double weight, double height) {
        this.weight = weight;
        this.height = height;
    }

    public double getWeight() {
        return weight;
    }

    public double getHeight() {
        return height;
    }

    
    
}
